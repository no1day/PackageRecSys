package com.entity;

public class Dish {
    
	private Integer did;
	
	private String dname;
	
	private String dpic; 
	
	private String description;
	
	private Integer count;
	
	private Integer dprice;
	
	private String dtype;
	
	private String food_material;
	
	public Integer getdid() {
		return did;
	}
	
	public void setdid(Integer did) {
		this.did = did;
	}
	
	public String getdname() {
		return dname;
	}
	
	public void setdname(String dname) {
		this.dname = dname;
	}
	
	public String getdpic() {
		return dpic;
	}
	
	public void setdpic(String dpic) {
		this.dpic = dpic;
	}
	
	public String getdescription() {
		return description;
	}
	
	public void setdescription(String description) {
		this.description = description;
	}
	
	public Integer getcount() {
		return count;
	}
	
	public void setcount(Integer count) {
		this.count = count;
	}
	
	public Integer getdprice() {
		return dprice;
	}
	
	public void setdprice(Integer dprice) {
		this.dprice = dprice;
	}
	
	public String getdtype() {
		return dtype;
	}
	
	public void setdtype(String dtype) {
		this.dtype = dtype;
	}
	
	public String getfood_material() {
		return food_material;
	}
	
	public void setfood_materiale(String food) {
		this.food_material = food;
	}
	
}
